# Create a Multi-Dimensional Dictionary
# Code by studyopedia

# Creating Dictionary1
mystock1 = {
    "Product": "SSD",
    "Price": 3000,
    "Quantity": 100,
    "InStock": "Yes"
}

# Creating Dictionary2
mystock2 = {
    "Product": "HDD",
    "Price": 2500,
    "Quantity": 50,
    "InStock": "Yes"
}

print(mystock1)
print(mystock2)

# Create a multi-dimensional Dictionary
products = {1:mystock1,2:mystock2}

print(products)