# Bitwise Operators in Python
# Code by studyopedia

a = 18 #18 = 0001 0010
b = 11 #11 = 0000 1011
res = 0

## Bitwise AND
res = a & b
print(res)

## Bitwise OR
res = a | b
print(res)

## Bitwise XOR
res = a ^ b
print(res)

## Bitwise NOT
res = ~a
print(res)

## Left shift bitwise operator
res = a << 3
print(res)

## Right shift bitwise operator
res = a >> 2
print(res)
